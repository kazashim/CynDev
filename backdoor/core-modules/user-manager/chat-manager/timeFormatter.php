<?php
function time_elapsed_string ($ptime) {
	$etime = time() - $ptime;

	if ($etime < 1) {
		return 'Now';
	}

	$a = array( 365 * 24 * 60 * 60  =>  'year',
						 30 * 24 * 60 * 60  =>  'month',
						 24 * 60 * 60  =>  'day',
						 60 * 60  =>  'hour',
						 60  =>  'minute',
						 1  =>  'second'
						);
	$a_plural = array( 'year'   => 'years',
										'month'  => 'months',
										'day'    => 'days',
										'hour'   => 'hours',
										'minute' => 'minutes',
										'second' => 'seconds'
									 );

	foreach ($a as $secs => $str)	{
		$d = $etime / $secs;
		if ($d >= 1) {
			$r = round($d);

			if ($str == "second") {
				return "Now";
			} else {
				return $r . ' ' . ($r > 1 ? $a_plural[$str] : $str) . ' ago';
			}
		}
	}
}
?>